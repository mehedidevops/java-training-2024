package com.syntexpro.variables;

public class DataTypes {


    public static void main(String[] args) {

        //variables
        //2 types
        //1. primitive
        //2. non-primitive

        //1. Primitive
        // 8 types

        //byte
        byte b = 7;
        System.out.println(b);

        //short
        short s = 7007;
        System.out.println(s);

        //int ******
        int d = 100_000_008;
        System.out.println(d);

        //long
        long i = 453453454545L;
        System.out.println(i);

        //float
        float j = 3.123457889012345F;
        System.out.println(j);

        //double ********
        double k = 10000000.80325545453452;
        System.out.println(k);

        //char
        char m = '&';
        System.out.println(m);

        //boolean **********
        boolean n = true;
        System.out.println(n);

        //Non-primitive
        //String ********************

        String p = "*12334%&Mehedi";
        System.out.println(p);




    }

}
